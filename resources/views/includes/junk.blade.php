                    <h2>Grin Mining </h2>
                </div>
                <div class="container">
                    <div class="tradingview-widget-container" style="margin:auto;">
                        <p>
                            2020 has been an exciting year for cryptocurrencies and we think there’s a good chance that
                            cryptocurrencies will enjoy a big boost in the near future. But making money in
                            cryptocurrency is not just about seeing the value of BTC rise, it is also about the
                            interesting new coins coming on to the cryptocurrency market – and one of these is Grin,
                            possibly one of the most exciting cryptocurrencies that's now on the market. <br> <br>

                            So, in this article we will outline what makes Grin different, and why it matters. We’ll
                            discuss the Grin protocol and what you need to do to mine the coin. In essence, Grin is a
                            cryptocurrency that uses a light blockchain and which operates privately – qualities that
                            put Grin in the news throughout 2020.
                        </p>

                        <h3>
                            So, what is Grin?
                        </h3>

                        <p>
                            The developers of Grin wants to make transacting online easy for everyone, without any risk
                            of censorship or any restrictions whatsoever. That’s why Grin is a cryptocurrency focused on
                            privacy. The technical skills of a Grin user shouldn’t matter, and the places where Grin
                            users live or their cultural mindset should have no affect on their ability to use Grin. The
                            developers behind Grin has also said that they want the Grin project to last a long time.
                            <br> <br>

                            A unique feature of Grin is that it hides both the sum of a transaction, and the addresses
                            associated with a transaction. It accomplishes this by sending data covering new
                            transactions privately amongst peers. The technical name for this procedure is a “random
                            walk”. So, the blocks in the Grin network do not contain information about individual
                            transactions. In fact, each Grin block has the appearance of a single big transaction.
                        </p>

                        <h3>
                            The main features of Grin
                        </h3>

                        <p>The developers of Grin had three main goals:</p>

                        <ul>
                            <li>
                                For Grin, the developers wanted a more spacious blockchain that removes the need to
                                constantly increase the size of the blockchain
                            </li>
                            <br>
                            <li>
                                Grin developers also wanted to ensure they could continue to comprehensively inspect the
                                blockchain
                            </li>
                            <br>
                            <li>
                                Finally, the developers of Grin wanted the ability to conduct private transactions
                            </li>
                        </ul>

                        <p>
                            The result: a privacy protocol called MimbleWimble, created by the developers of Grin in
                            order to meet the above needs. There is a Grin whitepaper that goes into thorough detail
                            about MimbleWimble. You’ll note that the whitepaper is dated in July 2016, an indication
                            that the Grin project has been underway for some time. Clearly, given how long the
                            developers has been working on Grin, quite a lot of effort has gone into developing Grin.
                            <br><br>

                            MimbleWimble brings a key feature to Grin – thanks to MimbleWimble Grin users can ensure
                            that a large part of the data that relate to old transactions can in fact be erased. So,
                            unlike other coins, Grin won’t lose its efficiency overtime, and become inoperable – a large
                            chunk of old data can be removed in the future. Furthermore, the blockchain data set for
                            Grin is smaller, so it doesn’t take as long to download by a new user.
                        </p>

                        <h3>
                            The funding behind Grin
                        </h3>

                        <p>
                            Yes, Grin is open-source and programmers across the globe work on developing Grin. There is
                            no company controlling Grin, nor does any single person have control over Grin. Furthermore,
                            Grin had a fair version of the typical cryptocurrency launch as no initial tokens were
                            placed during launch. Neither was there a pre-mining run and the developers behind Grin do
                            not get shares in block rewards. <br> <br>

                            Of course, every ideal sounding business model may cause problems in the future. For
                            example, at the start of 2020 one of the important developers for Grin, Igno Peverell, tried
                            to get some money for his work on the coin. Donations were not forthcoming and it resulted
                            in the speaker for the project making a complaint known to the Grin community. The result
                            was that EUR 55,000 was collected. Anyone can support Grin via the project’s official
                            website.
                        </p>

                        <h3>How has Grin performed?</h3>

                        <p>
                            Grin trades at about a dollar a coin, with a USD 28m market cap. About USD 25m in Grin
                            change hands every day, which is relative high trading volume. It may seem odd that the
                            turnover for Grin and the total market for Grin is about the same amount. Depending on when
                            you look you will find Grin to be around the 100th coin in the world. You might find it
                            interesting that Grin once fetched USD 261 per coin – on January 16, 2020.
                        </p>

                        <h3>Where can you buy Grin?</h3>

                        <p>
                            Hotbit is the leading exchange for Grin and counts for about 75% of the total number of Grin
                            coins that are exchanged. You have other options too, KuCoin and Gate.io for example also
                            offer Grin for trade in popular pairs including GRIN/USDT, GRIN/BTC and of course GRIN/ETH.
                        </p>

                        <h3>
                            Getting a Grin wallet
                        </h3>

                        <p>
                            The wallet for Grin isn’t great, unfortunately. First, Grin’s wallet does not come with a
                            graphical user interface (or GUI). Most modern wallets will have a GUI. Instead, you have to
                            access your Grin wallet using the command line in order to make transactions. <br> <br>

                            You start off by downloading the files you need – there is a set of files for MacOS and for
                            Linux (64bit, x86). You will also find complete instructions here. Even though the wallet
                            does not have a GUI the number of commands you need to enter is relatively few so it should
                            be easy to do. <br> <br>

                            To start you need to create the database of the wallet and create a seed file that’s secret.
                            You accomplish this using the following command line: <br> <br>

                            grin wallet init <br> <br>

                            You also need to add a password to your wallet. Keep in mind that you will need to enter
                            this password whenever you access the wallet so it’s not a password you want to forget. The
                            command line input you need for this process is: <br> <br>

                            File /home/yeastplume/.grin/grin-wallet.toml configured and created <br> <br>

                            Please enter a password for your new wallet <br> <br>

                            Password: <br> <br>

                            Confirm Password: <br> <br>

                            Next, every wallet must link up with a node, and it has to be synchronised fully. The
                            command to do this is: <br> <br>

                            grin wallet -r http://some_public_node.org:3413 info <br> <br>

                            If you want to you can replace the “-r” to instead read “-api_server_address”. Sending coins
                            is easy, you need to create a new transaction file and the require input is: <br> <br>

                            grin wallet send -m file -d my_grin_transaction.tx 10.25 <br> <br>

                            When you run this command you will find that the Grin wallet creates a file called
                            “my_grin_transaction.txt” in the folder in which you installed Grin. You need to send this
                            file to the person who will receive it – you can do it via FTP or email. The recipient then
                            needs to take steps to complete the transaction. There is a complete list of commands for
                            Grin, including sending and receiving instructions, on this Github page. <br> <br>

                            You might want to think about using an exchange if you want a more user friendly approach to
                            Grin. For example, Poloneix will make your life a lot easier – it offers a simple way to top
                            up your Grin balance using a URL to deposit funds. <br> <br>
                        </p>

                        <h3>
                            Mining Grin

                        </h3>

                        <p>
                            Grin can’t lay claim to popular crypto tech such as mastenodes or proof-of-stake, but it has
                            still created quite a lot of buzz. Why? One reason is its use of something called Cuckoo
                            Cycle which basically combines two proof-of-work algorithms: CuckAToo and CuckARoo. The
                            first one is designed for use by ASIC mining rigs, the other one is resistant to ASIC mining
                            and can only be mined using GPUs. <br> <br>

                            There is a key difference between the two – the graph size. Graph size has an impact on the
                            amount of GPU memory required to enable mining. For CuckARoo the graph size is 2 ^ 29, while
                            for CuckAToo the graph size is 2 ^ 31. That translates to 6GB and 11GB of video memory
                            respectively. It restricts what you can mine depending on your available GPU memory – you
                            might be able to mine only C29, or mine both C29 and C31. <br> <br>

                            The block reward for Grin is 60 coins, mining difficulty will vary depending on the overall
                            network hashrate. Block time is just about a minute. Note that, in July 2020, Grin had a
                            hard fork at block number 262,080 – at this time, Grin switched away from Cuckaroo29 and
                            adopted Cuckarood29. <br> <br>


                        </p>
                        <h3>Mining software for Grin</h3>

                        <p>
                            You can mine using AMD graphics cards, but the performance will be poor. We suggest you use
                            an Nvidia GPU, any GPU from the 6GB-equipped 1060, and up will be sufficient, the newly
                            released 20XX series will also work. <br> <br>

                            There is official mining software for Grin, you can download the Grin miner here. Your
                            alternatives include bminer which can mine Grin provided you download version 12.0.1 or
                            later, and of course GrinPro Miner and GrinGoldMiner, both of which has options for Linux
                            and Windows. If you definitely want to use an AMD GPU consider ePIC Boost Miner which can
                            run on a Sapphire RX570. <br> <br>

                            What hashrates can you expect for Grin? It depends on your GPU of choice of course, a
                            top-end GTX 1080Ti can achieve up to 8 gps, while the entry-level GTX 1060 will get you up
                            to 3 gps, but note you’d need 6GB of graphics RAM on it. If you’re wondering what gps is,
                            it’s Grin terminology, and stands for “graphs per second”.
                        </p>

                        <h3>
                            Your Grin mining options
                        </h3>

                        <p>
                            Mining in a pool is often more effective, we think the 2Miners Grin pool is a good place to
                            start. It pays out consistently and there are more than 8,000 miners participating. It’s an
                            easy to use interface too, and offers you the option to mine in SOLO or PPLNS modes. <br>
                            <br>

                            Furthermore, 2Miners supports both C29 and C31 versions of Cuckaroo. In fact, 2Miners can
                            automatically detect which one is in use in your miner and send the right job according to
                            the algorithm you are using. You can pick whichever one of the two algorithms you prefer,
                            2Miners will pay out rewards reflecting the underlying complexity.
                        </p>

                        <h3>
                            How to get started with Grin Mining
                        </h3>

                        <p>
                            You need to store your Grin somewhere, this could be the official Grin wallet stored locally
                            or you could opt to send your Grin earnings to a crypto exchange like HotBit, Poloniex or
                            BitMesh. Note that you will only get your Grin payouts if your wallet is active and online,
                            which is why using an exchange for receiving Grin receipts is a good idea. <br> <br>

                            You need to get a unique login from Grin, get this from the Grin “how to start” page. You
                            need to enter your GRIN address, it starts with either https or http. Next you need to
                            download your mining software, for example this read-to-go version. Add your login that you
                            retrieved from the Grin website to your miner’s settings. <br> <br>

                            To use Gminer with CuckARood-29 you need: <br> <br>

                            miner.exe --algo grin29 --server grin.2miners.com --port 3030 --user
                            2aHR0cHM7Ly9ncklucHJveHkubertZm9yZXg7Bd9tLzE5ODkzMzA.RIG_ID <br> <br>

                            To use lolMiner with CuckARood-29 use: <br> <br>

                            lolMiner.exe --coin GRIN-AD29 --pool grin.2miners.com --port 3030 --user
                            2aHR0cHM7Ly9ncklucHJveHkubertZm9yZXg7Bd9tLzE5ODkzMzA --pass x <br> <br>

                            Alternatively, the Gminer settings for CuckAToo-31 are: <br> <br>

                            miner.exe --algo grin31 --server grin.2miners.com --port 3030 --user
                            2aHR0cHM6Ly9ncmlucHJveHkuYml0Zm9yZXguY29tLzE5ODkzMzA.RIG_ID <br> <br>

                            Note that you have to replace the login string in our example with your own login, so
                            instead of 2aHR0cHM7Ly9ncklucHJveHkubertZm9yZXg7Bd9tLzE5ODkzMzA use your own login ID. <br>
                            <br>
                        </p>

                        <h3>
                            Cloud mining settings for Grin
                        </h3>

                        <p>
                            We’ll give you examples for two cloud mining providers too. First, let’s take a look at the
                            settings for Nicehash, utilising PPLNS: <br> <br>

                            ALGORITHM: GrinCuckarooC29 and GrinCuckatooC31 <br> <br>

                            POOL HOST: grin.2miners.com:3030 <br> <br>

                            WORKERNAME (-U): YOUR_LOGIN <br> <br>

                            PASSWORD (-P): x <br> <br>

                            Alternatively, if you rent your cloud mining service from MiningRigRentals, use: <br> <br>

                            ALGORITHM: Cuckaroo C29 (Grin) and Cuckaroo C31 (Grin) <br> <br>

                            POOL HOST: grin.2miners.com:3030 <br> <br>

                            WORKERNAME (-U): YOUR_LOGIN <br> <br>

                            PASSWORD (-P): x <br> <br>

                            If you want to, you can try and mine Grin solo but we suggest you change
                            grin.2miners.com:3030 and make it solo-grin.2miners.com:4040 instead. <br> <br>
                        </p>

                        <h3>
                            How profitable is Grin mining?
                        </h3>

                        <p>
                            Your mining profits will vary depending on a large range of factors. By example, if you use
                            a mining rig which contains eight 1080Ti GPUs that are mining using the C29 algorithm you’ll
                            find you get at the minimum 60 gps. If electricity costs you $0.05 for one kilo-watt hour
                            you should find a net profit of about $5 per day, or $7 if you exclude the cost of
                            electricity.
                        </p>

                        <h3>
                            Wrapping it up
                        </h3>

                        <p>
                            Look, Grin did drop in price dramatically soon after it’s release, but we still think it is
                            one of 2020’s most interesting and most profitable coins – it still offers huge potential.
                            Grin offers a high degree of anonymity while some would say it has attractive aspects of
                            “cyberpunk”. Executing a transaction by sending transaction files around is surely an
                            interesting way to transact. <br> <br>

                            It’s also worth keeping in mind that Grin gets solid funding and a big wave of support from
                            the community but the fact that the Grin interface works without a GUI does mean that Grin
                            might not become all that popular, not as popular as Bitcoin anyway. Some people might see
                            this as an advantage for Grin.
                        </p>


